var dropbtn = document.getElementById("dropbtn");
dropbtn.onmouseover = function(menu) {
    //faz o bloco aparecer
    menu = document.getElementById("dropMenu");
    menu.style.display = "initial";

}

dropbtn.onmouseover = function(menu) {
    menu = document.getElementById("dropMenu");
    menu.style.display = "none";
}